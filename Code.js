// ==================== SETTINGS (ScriptProperties) ====================
function getSettings() {
  const sp = PropertiesService.getScriptProperties();
  const json = sp.getProperty('VIEWER_SETTINGS');
  if (json) return JSON.parse(json);
  return createDefaultSettings();
}

function createDefaultSettings() {
  const defaults = {
    schoolName: 'School ERP Data Viewer',
    logoUrl: 'https://img.icons8.com/color/96/000000/school.png',
    primaryColor: '#1a237e',
    theme: 'light',
    customFooterLine: 'Generated by School ERP Viewer',
    contactEmail: ''
  };
  PropertiesService.getScriptProperties().setProperty('VIEWER_SETTINGS', JSON.stringify(defaults));
  return defaults;
}

function updateSettings(settingsObj) {
  const current = getSettings();
  const merged = { ...current, ...settingsObj };
  PropertiesService.getScriptProperties().setProperty('VIEWER_SETTINGS', JSON.stringify(merged));
  return { success: true };
}

function resetSettings() {
  PropertiesService.getScriptProperties().deleteProperty('VIEWER_SETTINGS');
  return createDefaultSettings();
}

function clearCache() {
  PropertiesService.getScriptProperties().deleteAllProperties();
  return { success: true, message: 'Cache cleared' };
}

