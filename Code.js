// ==================== CSV IMPORT ====================
function importCSVData(csvContent) {
  const lock = LockService.getScriptLock(); lock.waitLock(10000);
  try {
    const doc = SpreadsheetApp.openById(SPREADSHEET_ID);
    // Delete old import sheets
    doc.getSheets().forEach(sh => {
      if (sh.getName().startsWith(IMPORT_SHEET_PREFIX)) doc.deleteSheet(sh);
    });
    const sections = csvContent.split(/=== (.+?) ===/g).filter(Boolean);
    const imported = [];
    for (let i = 0; i < sections.length; i += 2) {
      const sheetName = IMPORT_SHEET_PREFIX + sections[i].trim();
      const csvPart = sections[i+1].trim();
      if (!csvPart) continue;
      const rows = Utilities.parseCsv(csvPart);
      if (rows.length === 0) continue;
      let sheet = doc.getSheetByName(sheetName);
      if (!sheet) sheet = doc.insertSheet(sheetName);
      else sheet.clear();
      sheet.getRange(1, 1, rows.length, rows[0].length).setValues(rows);
      imported.push({ name: sheetName, displayName: sections[i].trim(), rowCount: rows.length - 1 });
    }
    return { success: true, imported };
  } catch(e) {
    return { success: false, message: e.toString() };
  } finally {
    lock.releaseLock();
  }
}

function getImportedSheets() {
  const doc = SpreadsheetApp.openById(SPREADSHEET_ID);
  return doc.getSheets()
    .filter(sh => sh.getName().startsWith(IMPORT_SHEET_PREFIX))
    .map(sh => ({
      name: sh.getName(),
      displayName: sh.getName().replace(IMPORT_SHEET_PREFIX, ''),
      rowCount: sh.getLastRow() - 1
    }));
}

function getColumnsForSheet(sheetName) {
  const sheet = SpreadsheetApp.openById(SPREADSHEET_ID).getSheetByName(sheetName);
  if (!sheet) return [];
  return sheet.getRange(1, 1, 1, sheet.getLastColumn()).getValues()[0].filter(Boolean);
}

function getAllColumns() {
  const sheets = getImportedSheets();
  const allCols = new Set();
  sheets.forEach(sh => getColumnsForSheet(sh.name).forEach(c => allCols.add(c)));
  return Array.from(allCols).sort();
}

// ==================== FILTER / SEARCH ====================
function getFilteredData(sheetName, filters) {
  const sheet = SpreadsheetApp.openById(SPREADSHEET_ID).getSheetByName(sheetName);
  if (!sheet) return [];
  const data = sheet.getDataRange().getValues();
  if (data.length < 2) return [];
  const headers = data[0];
  let rows = data.slice(1).map(row => {
    const obj = {};
    headers.forEach((h, i) => obj[h] = row[i] || '');
    return obj;
  });

  if (filters.searchTerm) {
    const term = filters.searchTerm.toLowerCase();
    rows = rows.filter(r => Object.values(r).some(v => String(v).toLowerCase().includes(term)));
  }
  if (filters.column && filters.value) {
    rows = rows.filter(r => String(r[filters.column] || '').toLowerCase().includes(filters.value.toLowerCase()));
  }
  if (filters.dateField && (filters.dateFrom || filters.dateTo)) {
    rows = rows.filter(r => {
      const d = new Date(r[filters.dateField]);
      if (isNaN(d.getTime())) return false;
      if (filters.dateFrom && d < new Date(filters.dateFrom)) return false;
      if (filters.dateTo && d > new Date(filters.dateTo)) return false;
      return true;
    });
  }
  if (filters.class) rows = rows.filter(r => r.Class === filters.class);
  if (filters.section) rows = rows.filter(r => r.Section === filters.section);
  return rows;
}

function getGlobalSearchResults(filters) {
  const sheets = getImportedSheets();
  let all = [];
  sheets.forEach(sh => all = all.concat(getFilteredData(sh.name, filters)));
  return all;
}

function getPersonAllRecords(searchId) {
  const sheets = getImportedSheets();
  const results = {};
  sheets.forEach(sh => {
    const data = getFilteredData(sh.name, { searchTerm: searchId });
    if (data.length > 0) results[sh.displayName] = data;
  });
  return results;
}

